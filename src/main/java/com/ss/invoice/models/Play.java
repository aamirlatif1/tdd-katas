package com.ss.invoice.models;

public class Play {
    private String name;
    private String type;
    public Play() {
    }
    public void setName(String name) {
        this.name = name;
    }
    public void setType(String type) {
        this.type = type;
    }
    public String name() {
        return name;
    }
    public String type() {
        return type;
    }
}